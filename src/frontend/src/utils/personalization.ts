export type StoryItem = (typeof CURATED_STORIES)[0];
export type DiscoverItem = (typeof CURATED_DISCOVER)[0];
export type DiscoverItemWithLabel = DiscoverItem & { labelReason: string };
export type StoryItemWithPersonalized = StoryItem & { isPersonalized: boolean };

type VisitFrequencyRecord = {
  count: number;
  lastVisit: number;
  category?: string;
};

function capitalize(str: string): string {
  return str.charAt(0).toUpperCase() + str.slice(1);
}

// Scoring function for personalization
export function getPersonalizationScore(
  itemTags: string[],
  itemUrl: string,
  keywords: Record<string, number>,
  visitFrequency: Record<string, VisitFrequencyRecord>,
): number {
  let keywordScore = 0;

  // Keyword match score (max 30)
  for (const tag of itemTags) {
    const count = keywords[tag.toLowerCase()] ?? 0;
    keywordScore += count * 5;
  }
  keywordScore = Math.min(keywordScore, 30);

  // Visit recency score (max 20) - lower score for recently visited (fresh content)
  let visitRecencyScore = 20; // never visited = fresh content bonus
  const record = visitFrequency[itemUrl];
  if (record) {
    const hoursSinceVisit = (Date.now() - record.lastVisit) / (1000 * 60 * 60);
    visitRecencyScore = Math.max(0, 20 - record.count * 3);
    if (hoursSinceVisit < 1) visitRecencyScore = 0;
  }

  return keywordScore + visitRecencyScore;
}

export function getPersonalizedStories(
  keywords: Record<string, number>,
  visitFrequency: Record<string, VisitFrequencyRecord>,
): StoryItemWithPersonalized[] {
  const hasHistory =
    Object.keys(keywords).length > 0 || Object.keys(visitFrequency).length > 0;

  if (!hasHistory) {
    return [...CURATED_STORIES]
      .sort(() => Math.random() - 0.5)
      .slice(0, 12)
      .map((s) => ({ ...s, isPersonalized: false }));
  }

  return CURATED_STORIES.map((s) => {
    const score = getPersonalizationScore(s.tags, "", keywords, visitFrequency);
    return { ...s, score, isPersonalized: score > 0 };
  }).sort((a, b) => b.score - a.score);
}

export function getPersonalizedDiscover(
  keywords: Record<string, number>,
  visitFrequency: Record<string, VisitFrequencyRecord>,
  dismissedItemIds: string[],
  blockedSources: string[],
  blockedKeywordsUntil: Record<string, number>,
): DiscoverItemWithLabel[] {
  const now = Date.now();

  // Build set of currently blocked keyword/category keys
  const activeBlocks = new Set(
    Object.entries(blockedKeywordsUntil)
      .filter(([, expiry]) => expiry > now)
      .map(([key]) => key),
  );

  const hasHistory =
    Object.keys(keywords).length > 0 || Object.keys(visitFrequency).length > 0;

  const base = hasHistory
    ? CURATED_DISCOVER.map((d) => ({
        ...d,
        score: getPersonalizationScore(d.tags, d.url, keywords, visitFrequency),
      })).sort((a, b) => b.score - a.score)
    : [...CURATED_DISCOVER].sort(() => Math.random() - 0.5);

  // Filter dismissed, blocked sources, and blocked keyword/category items
  const filtered = base.filter((item) => {
    if (dismissedItemIds.includes(item.id)) return false;
    if (blockedSources.includes(item.source)) return false;
    if (activeBlocks.has(item.category.toLowerCase())) return false;
    if (item.tags.some((tag) => activeBlocks.has(tag.toLowerCase())))
      return false;
    return true;
  });

  // Compute label reason per item
  return filtered.map((item) => {
    let labelReason = "Suggested for you";

    // Check keyword match (first matching keyword not blocked)
    const matchedKeyword = item.tags.find((tag) => {
      const lowerTag = tag.toLowerCase();
      return (keywords[lowerTag] ?? 0) > 0 && !activeBlocks.has(lowerTag);
    });

    if (matchedKeyword) {
      labelReason = `Based on your search: ${capitalize(matchedKeyword)}`;
    } else {
      // Check category frequency match using visit records with a category field
      const categoryVisitCount = Object.values(visitFrequency)
        .filter((v) => v.category === item.category)
        .reduce((sum, v) => sum + v.count, 0);

      if (categoryVisitCount > 2) {
        labelReason = `Trending in ${capitalize(item.category)}`;
      }
    }

    return { ...item, labelReason };
  });
}
